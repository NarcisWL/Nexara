export const useI18n = () => {
    const language = useSettingsStore((state) => state.language) || 'zh';
    const hasHydrated = useSettingsStore((state) => state._hasHydrated);

    // 强制使用中文作为回退，防止 translations[undefined] 崩溃
    const currentLang = (language === 'en' || language === 'zh') ? language : 'zh';
    const t = translations[currentLang];

    return { t, language: currentLang, hasHydrated };
};
